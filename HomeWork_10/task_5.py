import math

earth = 10.8321 * 10 ** 11
r = float(input("Введите радиус инородной планеты "))
v = 4 * math.pi * (r ** 3) / 3
if v > earth:
    v = v / earth
    v = round(v , 3)
    print(f"Целых {v} Земель в одной этой планете")
elif v < earth:
    v = earth / v
    v = round(v, 3)
    print(f"Целых {v} планет поместиться в Земле")
else:
    print("Вот каков шанс найти планету такую же как Земля? Мне кажется он очень мал, но не смотря на это, ты умудрился это сделать.")