hours = int(input("Введите количество отработанных часов: "))
credit = int(input("Введите остаток по кредиту: "))
food = int(input("Введите траты на еду: "))

salary = ((200 * hours) / 2 ** 3 ) + hours
expenses = credit + food

if salary >= expenses:
  print("Тебе хватит, что бы выжить, но можно ещё немного поработать, что бы отложить.")
  print("Не хочешь испытать свои навыки в криптовалюте?")
else:
  print("Поработай больше! Тебе ещё как то-нужно выжить.")
  